--Adds vanilla files to proj/sprite pool
for i, proj in ipairs(projPoolVanilla) do
	table.insert(projPool, "data/entities/projectiles/" .. proj)
end

--Functions for corruption update file to pick a random file
function pickrandomspell(seed_x, seed_y)
	SetRandomSeed(seed_x + seed_y, seed_x * seed_y)
	local projFile = projPool[Random(1, #projPool)]
	return projFile
end

function pickrandomspellalt(seed_x, seed_y)
	SetRandomSeed(seed_x * seed_y, seed_x + 7 + seed_y)
	local projFile = projPool[Random(1, #projPool)]
	return projFile
end

